package ssrp.android.syncwhenonline.entity;

public class User {
	private String name;
	private int isSynced;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getIsSynced() {
		return isSynced;
	}

	public void setIsSynced(int isSynced) {
		this.isSynced = isSynced;
	}

}
